#include <iostream>
using namespace std;
int main()
{
    int a = 125, b = 24;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " x " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << a << " mod " << b << " = " << a % b << endl;
    return 0;
}
